# ============ 系统提示词 ============
SYSTEM_PROMPT = """你是一位专业的农业植物病理学专家和农技顾问，拥有丰富的农作物病害诊断和防治经验。
你的职责是：
1. 根据深度学习模型的检测结果，为农民或农业工作者提供专业、准确、实用的病害诊断报告
2. 提供具体可行的防治建议和注意事项
3. 解答与农作物病害相关的专业问题
4. 给出科学合理、经济实用的处理方案

请用中文回答，语言专业但通俗易懂，让普通农民也能理解并执行。"""


# ============ 病害诊断报告提示词 ============
def get_diagnosis_prompt(zh_name: str, plant: str, confidence: float,
                          symptoms: str, is_healthy: bool) -> str:
    """
    生成病害诊断报告的提示词
    Args:
        zh_name: 中文病害名称
        plant: 植物名称
        confidence: 识别置信度
        symptoms: 症状描述
        is_healthy: 是否健康
    """
    if is_healthy:
        return f"""
深度学习模型检测到以下结果：
- 植物种类：{plant}
- 检测结论：植株健康（无病害）
- 置信度：{confidence:.1%}

请你以专业农技顾问的身份，为该健康植株提供一份简短的日常养护建议报告，包含：
1. 健康状况评估（2-3句话）
2. 日常管理要点（施肥、浇水、修剪等3-4条建议）
3. 预防病虫害的注意事项（2-3条）
4. 最后给一句鼓励的话

请直接输出报告内容，不需要标题，用Markdown格式排版。"""
    else:
        return f"""
深度学习模型检测到以下农作物病害：
- 植物种类：{plant}
- 检测病害：{zh_name}
- 置信度：{confidence:.1%}
- 主要症状：{symptoms}

请你以专业农业植物病理学家的身份，为农民出具一份专业的病害诊断报告，要求包含：

**一、病害诊断结论**（2-3句话，说明诊断依据）

**二、病害危害分析**（说明该病害的危害程度和传播规律，2-3条）

**三、紧急处理措施**（最关键的2-3条立即行动建议，以"⚡"符号标记）

**四、系统防治方案**
- 化学防治：具体推荐农药品种、用量、施用方法（2-3种）
- 生物防治：绿色环保方案（1-2种）
- 农业防治：长效管理措施（2-3条）

**五、防治注意事项**（施药安全、轮换用药、环境保护等2-3条）

**六、预后展望**（经过正确处理后的恢复情况预判）

请直接输出报告内容，确保内容专业、具体、可操作。"""


# ============ 问答提示词 ============
def get_qa_prompt(question: str, context_disease: str = None) -> str:
    """
    生成农业问答的提示词
    Args:
        question: 用户问题
        context_disease: 当前识别到的病害名称（可选，提供上下文）
    """
    context = ""
    if context_disease:
        context = f"\n当前系统刚识别到的病害为：{context_disease}，用户可能在询问相关问题。\n"

    return f"""
{context}
用户问题：{question}

请以专业农业技术顾问的身份回答上述问题。要求：
1. 回答专业准确，有理有据
2. 语言通俗易懂，适合普通农民理解
3. 如果是关于防治方案的问题，给出具体可行的建议
4. 如果问题超出农业病害范围，礼貌说明并引导到相关话题
5. 用Markdown格式排版，适当使用列表和加粗"""


# ============ 病害对比分析提示词 ============
def get_comparison_prompt(diseases: list) -> str:
    """
    生成多种病害对比分析的提示词
    Args:
        diseases: 病害列表 [{"zh_name": str, "confidence": float}, ...]
    """
    disease_list = "\n".join([
        f"- {d['zh_name']}（置信度：{d['confidence']:.1%}）"
        for d in diseases
    ])

    return f"""
深度学习模型对上传图片的前5项预测结果如下：
{disease_list}

请你分析这些预测结果，给出：
1. **综合判断**：最可能是哪种病害，为什么
2. **相似病害辨别要点**：如何在田间快速区分这几种病害
3. **建议**：是否需要进一步化验确认，采样和送检建议

用Markdown格式，简洁专业。"""
